// O(n) - Linear Time Complexity - Kako raste niz, rasce i vreme izvrsavanja
export const sum_index_O_n = (n) => {
	let sum = 0; // Executed 1

	for (let i = i; i <= n; i++) {
		sum += i; // Executed n times
	}

	return sum; // Executed 1
};

